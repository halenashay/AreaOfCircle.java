public class AreaOfCircle {
    public static void main(String[] args) {

        //Declare variables
        double pi = 3.14;
        int radius = 5;
        double area = 0;
        area = pi * (radius * radius);

        //Print statements
        System.out.println("Radius of the circle is : " + radius);
        System.out.println("The area of the circle is : " + area);
    }
}
